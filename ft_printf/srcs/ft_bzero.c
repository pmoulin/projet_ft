#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*tmp;

	tmp = (char *)s;
	i = 0;
	while (n != 0)
	{
		tmp[i] = '\0';
		n--;
		i++;
	}
}
